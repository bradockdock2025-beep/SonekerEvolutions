          y += 1.5

          doc.setDrawColor(tr, tg, tb)
          doc.setLineWidth(0.4)
          doc.line(M + 7, y, M + 7 + 44, y)
          y += 4

          const body = clean(card.body)
          if (body) { put(body, M + 7, CW - 7, 10, 'normal', 40, 40, 65); y += 3 }

