'use client'

import { useToast } from '@/context/ToastContext'
import { useApp } from '@/context/AppContext'
import { useLanguage } from '@/context/LanguageContext'
import { useAuth } from '@/context/AuthContext'
import type { AnalysisResult } from '@/types'

function buildMarkdown(result: AnalysisResult, vocabLabel: string): string {
  const stripHtml = (html: string) => html.replace(/<[^>]+>/g, '')
  const lines: string[] = []

  lines.push(`# ${result.videoTitle}`)
  lines.push('')
  lines.push(`**Channel:** ${result.channel}`)
  if (result.niche) lines.push(`**Niche:** ${result.niche}`)
  lines.push('')
  lines.push('---')
  lines.push('')

  const sections = [...new Set(result.cards.map(c => c.section))]
  for (const section of sections) {
    const cards = result.cards.filter(c => c.section === section)
    lines.push(`## ${cards[0].badge}`)
    lines.push('')
    for (const card of cards) {
      lines.push(`### ${card.name}`)
      lines.push('')
      lines.push(stripHtml(card.body))
      lines.push('')
      if (card.tags.length) lines.push(`**Tags:** ${card.tags.join(', ')}`)
      lines.push('')
      lines.push(`**${card.deepPanel.title}**`)
      for (const row of card.deepPanel.rows) {
        if (row.value) lines.push(`- **${row.label}:** ${row.value}`)
        if (row.tags?.length) lines.push(`- **${row.label}:** ${row.tags.join(', ')}`)
      }
      lines.push('')
    }
  }

  if (result.vocabulary.length) {
    lines.push('---')
    lines.push('')
    lines.push(`## ${vocabLabel}`)
    lines.push('')
    for (const v of result.vocabulary) {
      lines.push(`### ${v.term}`)
      lines.push(v.definition)
      if (v.points?.length) {
        lines.push('')
        v.points.forEach(p => lines.push(`- ${p}`))
      }
      lines.push('')
    }
  }

  lines.push('---')
  lines.push('')
  lines.push('*Generated by [Soneker](https://soneker.app)*')

  return lines.join('\n')
}

function downloadText(content: string, filename: string, mime: string) {
  const blob = new Blob([content], { type: mime })
  const url = URL.createObjectURL(blob)
  const a = document.createElement('a')
  a.href = url
  a.download = filename
  a.click()
  URL.revokeObjectURL(url)
}

export default function ExportBar() {
  const { showToast } = useToast()
  const { setMindMapOpen, result, saveAnalysis, setScreen } = useApp()
  const { t } = useLanguage()
  const { user } = useAuth()

  const handleSave = async () => {
    if (!result) { showToast(t('exp_toast_no_save')); return }
    if (!user) { setScreen('signin'); return }
    try {
      await saveAnalysis()
      showToast(t('exp_toast_saved'))
    } catch {
      showToast(t('exp_toast_save_error'))
    }
  }

  const handlePdf = () => {
    if (!result) { showToast(t('exp_toast_no_save')); return }
    window.print()
  }

  const handleMarkdown = () => {
    if (!result) { showToast(t('exp_toast_no_save')); return }
    const md = buildMarkdown(result, t('exp_vocab_label'))
    const slug = result.videoTitle.slice(0, 40).replace(/[^a-z0-9]/gi, '-').toLowerCase()
    downloadText(md, `soneker-${slug}.md`, 'text/markdown')
    showToast(t('exp_toast_markdown'))
  }

  const handleCopy = () => {
    if (!result) { showToast(t('exp_toast_no_copy')); return }
    const md = buildMarkdown(result, t('exp_vocab_label'))
    navigator.clipboard.writeText(md).then(() => showToast(t('exp_toast_copied')))
  }

  return (
    <div className="exp-bar">
      <button className="exp-btn p" onClick={handleSave}>
        <svg width="11" height="11" viewBox="0 0 11 11" fill="none">
          <path d="M1 8.5V10h1.5l4.5-4.5-1.5-1.5L1 8.5zM9.4 2.1l-1.5-1.5a.5.5 0 00-.7 0L6.1 1.7l1.5 1.5 1.8-1.1z" fill="currentColor"/>
        </svg>
        {t('exp_save')}
      </button>
      <button className="exp-btn" onClick={handlePdf}>
        <svg width="11" height="11" viewBox="0 0 11 11" fill="none">
          <path d="M2 1h5l2.5 2.5V10a.8.8 0 01-.8.8H2a.8.8 0 01-.8-.8V1.8A.8.8 0 012 1z" stroke="currentColor" strokeWidth="1.1" strokeLinejoin="round"/>
          <path d="M6.5 1v3h3M5.5 5.5v3M4 7l1.5 1.5L7 7" stroke="currentColor" strokeWidth="1.1" strokeLinecap="round" strokeLinejoin="round"/>
        </svg>
        {t('exp_pdf')}
      </button>
      <button className="exp-btn" onClick={handleCopy}>
        <svg width="11" height="11" viewBox="0 0 11 11" fill="none">
          <rect x="3.5" y="3.5" width="6" height="6" rx="1.5" stroke="currentColor" strokeWidth="1.1"/>
          <path d="M1 7.5V1h6.5" stroke="currentColor" strokeWidth="1.1" strokeLinecap="round"/>
        </svg>
        {t('exp_copy')}
      </button>
      <button className="exp-btn" onClick={() => showToast(t('exp_toast_notion'), '→')}>
        <svg width="11" height="11" viewBox="0 0 11 11" fill="none">
          <rect x="1" y="1" width="9" height="9" rx="2" stroke="currentColor" strokeWidth="1.1"/>
          <path d="M3.5 3.5h4M3.5 5.5h3M3.5 7.5h2" stroke="currentColor" strokeWidth="1.1" strokeLinecap="round"/>
        </svg>
        {t('exp_notion')}
      </button>
      <button className="exp-btn" onClick={handleMarkdown}>
        <svg width="11" height="11" viewBox="0 0 11 11" fill="none">
          <path d="M1 3h9M1 5.5h5M1 8h7" stroke="currentColor" strokeWidth="1.1" strokeLinecap="round"/>
        </svg>
        {t('exp_markdown')}
      </button>
      <button className="exp-btn" onClick={() => setMindMapOpen(true)}>
        <svg width="11" height="11" viewBox="0 0 11 11" fill="none">
          <circle cx="5.5" cy="5.5" r="1.8" stroke="currentColor" strokeWidth="1.1"/>
          <circle cx="1.5" cy="1.5" r=".9" stroke="currentColor" strokeWidth="1"/>
          <circle cx="9.5" cy="1.5" r=".9" stroke="currentColor" strokeWidth="1"/>
          <circle cx="1.5" cy="9.5" r=".9" stroke="currentColor" strokeWidth="1"/>
          <circle cx="9.5" cy="9.5" r=".9" stroke="currentColor" strokeWidth="1"/>
          <path d="M3.2 3.2l1 1M6.3 6.3l1 1M7.8 3.2l-1 1M4.7 6.3l-1 1" stroke="currentColor" strokeWidth="1" strokeLinecap="round"/>
        </svg>
        {t('exp_mindmap')}
      </button>
    </div>
  )
}
