function buildMarkdown(result: AnalysisResult, vocabLabel: string): string {
  const stripHtml = (html: string) => html.replace(/<[^>]+>/g, '')
  const lines: string[] = []

  lines.push(`# ${result.videoTitle}`)
  lines.push('')
  lines.push(`**Channel:** ${result.channel}`)
  if (result.niche) lines.push(`**Niche:** ${result.niche}`)
  lines.push('')
  lines.push('---')
  lines.push('')

  const sections = [...new Set(result.cards.map(c => c.section))]
  for (const section of sections) {
    const cards = result.cards.filter(c => c.section === section)
    lines.push(`## ${cards[0].badge}`)
    lines.push('')
    for (const card of cards) {
      lines.push(`### ${card.name}`)
      lines.push('')
      lines.push(stripHtml(card.body))
      lines.push('')
      if (card.tags.length) lines.push(`**Tags:** ${card.tags.join(', ')}`)
      lines.push('')
      lines.push(`**${card.deepPanel.title}**`)
      for (const row of card.deepPanel.rows) {
        if (row.value) lines.push(`- **${row.label}:** ${row.value}`)
        if (row.tags?.length) lines.push(`- **${row.label}:** ${row.tags.join(', ')}`)
      }
      lines.push('')
    }
  }

  if (result.vocabulary.length) {
    lines.push('---')
    lines.push('')
    lines.push(`## ${vocabLabel}`)
    lines.push('')
    for (const v of result.vocabulary) {
      lines.push(`### ${v.term}`)
      lines.push(v.definition)
      if (v.points?.length) {
        lines.push('')
        v.points.forEach(p => lines.push(`- ${p}`))
      }
      lines.push('')
    }
  }

  lines.push('---')
  lines.push('')
  lines.push('*Generated by [Soneker](https://soneker.app)*')

  return lines.join('\n')
}

function downloadText(content: string, filename: string, mime: string) {
  const blob = new Blob([content], { type: mime })
  const url = URL.createObjectURL(blob)
  const a = document.createElement('a')
  a.href = url
  a.download = filename
  a.click()
  URL.revokeObjectURL(url)
}

